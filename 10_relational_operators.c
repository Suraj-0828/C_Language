#include <stdio.h>
int main(){
	// relational operators
	int num1 = 200;
	int num2 = 400;
	
	// 0 = F
	// 1 = T
	
	printf("%d \n",(num1 < num2));
	printf("%d \n",(num1 > num2));
	printf("%d \n",(num1 == num2));
	printf("%d \n",(num1 != num2));
	printf("%d \n",(num1 <= num2));
	printf("%d \n",(num1 >= num2));
}
