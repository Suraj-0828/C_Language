#include <stdio.h>
int main(){
	//arithmetic operators
	
	int num1 = 500;
	int num2 = 200;
	
	printf("arithmetic operators + %d \n ",(num1+num2));
	printf("arithmetic operators - %d \n ",(num1-num2));
	printf("arithmetic operators / %d \n ",(num1/num2));
	printf("arithmetic operators * %d \n ",(num1*num2));
	printf("arithmetic operators percent %d \n ",(num1%num2));
	return 0;
}
